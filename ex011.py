#Faça um programa que leia a largura e a altura de uma parede em metros, calcule a sua área e a quantidade de tinta nececssária
# para pintá-la,sabendo que cada litro de tinta pinta uma área de 2m²

largura=float(input('qual largura da parede em metros? '))
altura=float(input('qual altura da parede em metros? '))
print(f'você possui {largura*altura} metros quadrados de parede\nEntão precisa de {(largura*altura)/2} litros de tinta!!')



